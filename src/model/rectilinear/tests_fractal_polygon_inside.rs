fn get_matrix_with_boundary(
    polygon: &Polygon,
    max_x: usize,
    max_y: usize,
) -> (Vec<Vec<bool>>, Vec<Point>) {
    let mut matrix = vec![vec![false; max_y]; max_x];
    let mut boundary = Vec::new();

    // TODO: More efficient solution.
    for x in 0..max_x {
        for y in 0..max_y {
            let point = Point::new(x as i32, y as i32);
            if polygon.is_on_edge(&point) {
                matrix[x][y] = true;
                boundary.push(point);
            }
        }
    }

    (matrix, boundary)
}

fn flood_fill(matrix: &Vec<Vec<bool>>, inside_point: Point) -> Vec<Point> {
    let mut inside_points = HashSet::new();
    inside_points.insert(inside_point);

    let mut queue = VecDeque::new();
    queue.push_back(inside_point);

    let mut visited = HashSet::new();

    while let Some(point) = queue.pop_front() {
        if visited.contains(&point) {
            continue;
        }

        visited.insert(point);
        for dir in &[
            Direction::UP,
            Direction::LEFT,
            Direction::DOWN,
            Direction::RIGHT,
        ] {
            let neigh = point.add(dir.to_point());
            let is_edge = matrix[neigh.x as usize][neigh.y as usize];

            if !is_edge {
                queue.push_back(neigh);
                inside_points.insert(neigh);
            }
        }
    }

    // To ensure deterministic iteration later, we return a sorted vector.
    let mut inside_points_vec = inside_points.into_iter().collect::<Vec<Point>>();
    inside_points_vec.sort_by(|p1, p2| p1.x.cmp(&p2.x).then(p1.y.cmp(&p2.y)));
    inside_points_vec
}

